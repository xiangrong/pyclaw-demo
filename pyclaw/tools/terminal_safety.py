from __future__ import annotations

import os
import re
import shlex
from dataclasses import dataclass
from typing import Iterable


@dataclass(frozen=True)
class LocalPathReference:
    """A shell-aware local filesystem reference found in a command.

    ``path`` is the token as generated by the model. ``resolved_path`` is the
    path resolved against the shell's current directory at that point in the
    command.  The extractor intentionally reports only arguments that are used
    by local filesystem commands/redirections; quoted remote commands and code
    strings are left opaque so Android/Linux paths such as ``/system/bin/sh`` or
    ``/proc/stat`` are not mistaken for host filesystem access.
    """

    path: str
    resolved_path: str
    reason: str


